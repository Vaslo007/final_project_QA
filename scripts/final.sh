echo "Проверка возраста"
echo "----------------"

# Запрос возраста
read -p "Введите ваш возраст: " age

# Проверка что введено число
if ! [[ "$age" =~ ^[0-9]+$ ]]; then
    echo "Ошибка: пожалуйста, введите целое число!" >&2
    exit 1
fi

# Проверка возрастных категорий
if [ "$age" -lt 0 ]; then
    echo "Возраст не может быть отрицательным!"
elif [ "$age" -lt 13 ]; then
    echo "Вам $age лет. Вы ребенок."
elif [ "$age" -lt 18 ]; then
    echo "Вам $age лет. Вы подросток."
elif [ "$age" -lt 60 ]; then
    echo "Вам $age лет. Вы взрослый."
elif [ "$age" -lt 100 ]; then
    echo "Вам $age лет. Вы пожилой человек."
else
    echo "Вам $age лет. Долгожитель!"
fi
